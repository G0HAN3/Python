print(5+6)    #Addition
print(15-6)   #Subtraction
print(15*6)   #Multiplication
print(15/6)   #Division
print(15//6)  #Floor Division	
print(5%3)    #Modulus
print(2**4)   #Exponential	


# Typecasting
a = "1"
# a = 1
b = "2"
# b = 2
print(int(a) + int(b))

# Implicit TypeCasting
c = 1.9
d = 8
# Python typecasts lower order datatype to higher order datatype to prevent data loss
print(c + d)

